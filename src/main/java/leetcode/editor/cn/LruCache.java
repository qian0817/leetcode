    class LRUCache {
        private final int capacity;
        private Map<Integer, Node> map = new HashMap<>();
        private Node head = new Node(0, 0);
        private Node tail = new Node(0, 0);
        public LRUCache(int capacity) {
            this.capacity = capacity;
            head.next = tail;
            tail.prev = head;
        }

        public int get(int key) {
            Node node = map.get(key);
            if (node == null) {
                return -1;
            }
            deleteNode(node);
            addHead(node);
            return node.value;
        }

        public void put(int key, int value) {
            Node node = map.get(key);
            if (node != null) {
                deleteNode(node);
            } else {
                node = new Node(key, value);
            }
            addHead(node);
            map.put(key, node);
            if (map.size() > capacity) {
                Node last = tail.prev;
                deleteNode(last);
                map.remove(last.key);
            }
        }

        private void addHead(Node node) {
            Node next = head.next;
            next.prev = node;
            node.next = next;
            head.next = node;
            node.prev = head;
        }

        private void deleteNode(Node last) {
            Node prev = last.prev;
            Node next = last.next;
            prev.next = next;
            next.prev = prev;
            last.next = null;
            last.prev = null;
        }

        static class Node {
            final Integer key;
            final Integer value;
            Node next;
            Node prev;

            public Node(Integer key, Integer value) {
                this.key = key;
                this.value = value;
            }
        }
    }

